import mongoose from "mongoose";
import { dbCallTrackerPlugin } from "../utils/dbCallTracker.js";

const userSchema = new mongoose.Schema({
    name: {
        type: String,
        required: true,
        trim: true
    },
    email: {
        type: String,
        required: true,
        unique: true,
        trim: true,
        lowercase: true,
        index: true
    },
    password: {
        type: String,
        required: true,
        trim: true
    },
    role: {
        type: String,
        enum: ['user', 'admin'],
        default: 'user'
    },
    createdAt: {
        type: Date,
        default: Date.now
    },
});

// Apply DB call tracking plugin
userSchema.plugin(dbCallTrackerPlugin);

const User = mongoose.model("User", userSchema);

export default User;